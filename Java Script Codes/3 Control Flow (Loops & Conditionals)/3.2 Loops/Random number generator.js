// generating random number between 1 to 100
// 1 = min
// 100 = max
let randomNumberBetween=Math.floor(Math.random()*(100-1+1))+1;
console.log("Random number between 1 to 100 : ",(randomNumberBetween));

let randomNumberBetween2=Math.floor(Math.random()*(500-50+1))+50;
console.log("Random number between 50 to 500 : ",(randomNumberBetween2));